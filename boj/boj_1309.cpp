#include <iostream>
#include <vector>
#include <queue>
using namespace std;

int N;
int dp[100001];
int main()
{
	cin >> N;
	dp[1] = 3;
	dp[2] = 7;
	dp[3] = 17;

	for (int i = 4; i <= N; i++)
	{
		dp[i] = (dp[i - 1] * 2 + dp[i - 2]) % 9901;
	}

	cout << dp[N];
}

